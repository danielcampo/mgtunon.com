					<h1>Cambiar de Carrera - March 3, 2011</h1>
					
					<p><strong>Cambiar de trabajo es siempre estresante, pero junto con la incertidumbre econ&#243;mica que existe hoy en d&#237;a, puede ser algo aterrador!  A pesar de esto, hay muchas personas que se encuentran en una situaci&#243;n en la cual tienen que contemplar cambiar no solo de trabajo, pero quiz&#225;s cambiar de carrera.</strong></p>

					<p><strong>&#191;Es ahora un buen momento para cambiar de carrera?</strong></p>
					<p>La respuesta a esa pregunta es si y no&#8230;depende.  Primero, tenemos que estar consientes de que en estos momentos&#8230;</p>


					<ul>
						<li>hay menos trabajos disponibles que en otros tiempos</li>

						<li>empleadores no est&#225;n dispuestos a tomar riesgos (as&#237; que prefieren &#8220;ir a lo seguro&#8221; y contratar a personas con experiencia)</li>

						<li>hay muchas personas calificadas buscando trabajo</li>
					</ul>



					<p>Por estas razones la respuesta seria no&#8230; no es un buen tiempo para cambiar de carrera.</p>

