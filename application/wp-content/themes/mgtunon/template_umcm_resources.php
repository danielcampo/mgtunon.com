						<h1>De Regreso Al Colegio - March 17, 2011</h1>
					
						<p><strong>Sabemos que muchas personas est&#225;n pensando en regresar al colegio despu&#233;s de muchos a&#241;os de estar trabajando.  Hay aquellos que nunca terminaron su educaci&#243;n formal y siempre se quedaron con las ganas.  Otros buscan un aumento de sueldo o una promoci&#243;n.  Y aun otros est&#225;n en busca de una nueva carrera la cual requiere nuevos talentos que es capaz que puedan encontrar en la escuela.  Hoy voy a enfocarme en una pregunta crucial, &#191;Le ayudar&#237;a a mi carrera o mi negocio si yo regresar&#237;a al colegio?</strong></p>

						<p><strong>Regresar al colegio es una decisi&#243;n compleja y seria.  &#191;C&#243;mo determina uno si el regresar al colegio es lo correcto? &#191;Si vale la pena?</strong>
							<br />
						Cada caso es diferente, as&#237; que no ser&#237;a posible dar un criterio que les sirva a todos. Pero si hay algunas consideraciones que pueden tomar en cuenta al momento de tomar una decisi&#243;n.  Te debes preguntar:</p>

					<ol>
						<li>&#191;Cu&#225;nto me va costar mi nueva educaci&#243;n en comparaci&#243;n al posible aumento de salario que gane despu&#233;s de obtener mi t&#237;tulo?></li>

						<li>&#191;C&#243;mo le va impactar esto a mi familia? &#191;Al corto plazo? &#191;Al largo plazo?</li>

						<li>&#191;Cu&#225;les trabajos est&#225;n disponibles para aquellos con este t&#237;tulo?</li>

						<li>&#191;Qu&#233; provecho le sacar&#237;a a mi educaci&#243;n comparada con la experiencia que ya tengo en mi carrera o negocio?</li>

					</ol>

						<p>En s&#237;, lo m&#225;s importante es que tengan muy claro en mente sus prioridades.  El regresar al colegio es una gran meta, una que probablemente le requerir&#225; sacrificar prioridades menos importantes.</p>

						<p><strong>Ahora, los estudiantes en las universidades son por la mayor parte j&#243;venes de 18 a 22 a&#241;os de edad.  &#191;No se sentir&#237;a uno de mayor edad un poco extra&#241;o al estar rodeado de compa&#241;eros mucho menor de edad?</strong>
						<br />
					
						Esto es un sentimiento muy com&#250;n, pero no refleja la realidad.  Hay m&#225;s y m&#225;s adultos regresando al colegio.  De acuerdo al Censo, al partir del 2007, los adultos mayores de 37 anos formaron 15% de la educaci&#243;n universitaria. Y tomen en cuenta que los profesores aprecian a los estudiantes que tienen mucha experiencia, ya que pueden compartir esas experiencias con otros estudiantes que solamente han estado expuestos a la teor&#237;a en la clase.  Es m&#225;s, esta vez le sacaras m&#225;s provecho, ya que tienes en mente el provecho que le quieres sacar al t&#237;tulo.</p>

						<p><strong>&#191;Para aquellos que no tengan manera de financiar los costos de la universidad, que recursos pueden usar para ayudarles cumplir sus sue&#241;os?</strong>
						<br />
						Cr&#233;elo o no, hay bastantes recursos disponibles.  Les voy a sugerir tres que siento que son los m&#225;s alcanzables:</p>

					<ol>
					
						<li>Puedes que califiques para ayuda financiera del gobierno, aun si no est&#225;s en el colegio &#8220;full time.&#8221;  Para ver si calificas, busque en <a href="www.FederalStudentAid.ed.gov">www.FederalStudentAid.ed.gov</a>.</li>

						<li>Para poder analizar una variedad de becas y programas de financiamiento de matricula pueden visitar Fast Web Scholarship Search &#8211; <a href="www.fastweb.com">www.fastweb.com</a></li>

						<li>Tambi&#233;n hay recursos y becas espec&#237;ficamente para los hispanos &#8211; para mas informaci&#243;n pueden visitar al Hispanic Scholarship Fund &#8211; <a href="www.hsf.net">www.hsf.net</a></li>

						<li>Tambi&#233;n es posible que consigas la asistencia de tu empleador.  Si tu educaci&#243;n es relevante a tu empleo, tu compa&#241;&#237;a puede tener un programa de matriculaci&#243;n donde te reembolsan todos o parte de los gastos.</li>

					</ol>
						<p>Tambi&#233;n acu&#233;rdense, que el regresar a la universidad no es la &#250;nica opci&#243;n.  Hoy en d&#237;a, hay cursos que pueden tomar a trav&#233;s de la computadora a las horas del d&#237;a que les convenga.  Muchas universidades tambi&#233;n ofrecen cursos por las noches o los fines de semana.  Esto les permite mantener su empleo y terminar un titulo o curso de modo flexible.</p>
					
